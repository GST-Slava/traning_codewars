function add(a,b){
    return a + b
}//добавить
function divide(a,b){
    return a / b
}//разделить
function multiply(a,b){
    return a * b
}//умножить
function mod(a,b){
    return a % b
}//деление по модулю или остаток от деления
function exponent(a,b){
    return a ** b
}//возведение в степень
function subt(a,b){
    return a - b
}//вычитание

